#include "libmat.h"

vec3  vec3_cross(vec3 *u, vec3 *v)
{
    vec3  ret;

    ret.x = u->y * v->z - u->z * v->y;
    ret.y = u->z * v->x - u->x * v->z;
    ret.z = u->x * v->y - u->y * v->x;
    return (ret);
}
